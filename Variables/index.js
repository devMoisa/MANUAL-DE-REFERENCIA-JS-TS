// Just declaring/initializing the variables.
var value1;
let value2;

// Setting a value into initialized/declared variables.
value1 = "Value 1";
value2 = "Value 2";

// Constants can't be initialized and receive a new value after.
const value3 = "Value 3";

// Loggins
console.log("var: ", value1);
console.log("let: ", value2);
console.log("const: ", value3);
